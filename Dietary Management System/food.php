      <h3>Food</h3>
      <iframe width="560" height="315" src="https://www.youtube.com/embed/v13o_AMW_wI" frameborder="0" allowfullscreen></iframe>
        <br>
      <p>Delicious foods that help you diet? It sounds too good to be true.

No doubt: Weight loss comes down to simple math. You have to eat fewer calories than you burn.

"Certain foods can help you shed body weight," says Peter Karanja, MD, a spokesman for the Academy of Nutrition and Dietetics, "because they help you feel full longer and help curb cravings."

Some even kick up your metabolism. So take this list when you go to the supermarket:

</p>

      <h4>1.Pureed Vegetables</h4>
           <p>You can add more veggies to your diet, enjoy your "cheat" foods, and cut back on the calories you’re eating, all at the same time. When Penn State researchers added pureed cauliflower and zucchini to mac and cheese, people seemed to like the dish just as much. But they ate 200 to 350 fewer calories. Those healthy vegetables added low-cal bulk to the tasty dish.

</p>

        <h4>2.Soup</h4>
      <p>Start a meal with a cup of soup, and you may end up eating less. It doesn’t matter if the soup is chunky or pureed, as long as it's broth-based. You want to keep the soup to 100 to 150 calories a serving. So skip the dollops of cream and butter.

</p>

        <h4>3.Beans</h4>
        <p>Inexpensive, filling, and versatile, beans are a great source of protein. Beans are also high in fiber and slow to digest. That means you feel full longer, which may stop you from eating more.</p>
        
        <h4>4.Eggs and Sausage</h4>
        <p>A protein-rich breakfast may help you resist snack attacks throughout the day.

In a study of a group of obese young women, those who started the day with 35 grams of protein -- that’s probably way more than you’re eating -- felt fuller right away. The women ate a 350-calorie breakfast that included eggs and a beef sausage patty. The effect of the high-protein breakfast seemed to last into the evening, when the women munched less on fatty, sugary goods than the women who had cereal for breakfast.</p>
        
        <h4>5.Dark Chocolate</h4>
        <p>Want to enjoy chocolate between meals? Pick a square or two of dark over the milky version. In one study, chocolate lovers who were given dark chocolate ate 15% less pizza a few hours later than those who had eaten milk chocolate.

</p>
        
        <h4>6.Nuts</h4>
        <p>For a great snack on the run, take a small handful of almonds, peanuts, walnuts, or pecans. Research shows that when people munch on nuts, they automatically eat less at later meals.

</p>
        
        <h4>7.Yogurt</h4>
        <p>Whether you prefer Greek or traditional, yogurt can be good for your waistline.

A Harvard study followed more than 120,000 people for a decade or longer. Yogurt, of all the foods that were tracked, was most closely linked to weight loss.

That doesn't prove that yogurt caused weight loss, but it stood out among other foods.

</p>
      
      
      
      
